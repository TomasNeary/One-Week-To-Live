require "ISUI/ISPanel"
----------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------

em_progressBar = {
	bg = {r=0.25, g=0.25, b=0.25, a=1},
	bar = {r=1, g=0, b=0, a=1},
	border = {r=1, g=1, b=1, a=1},
};

----------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------

em_progressBar.render = function(_panel, _x, _y, _w, _h, _value)
	local _value = math.floor(_value * 100) / 10000;
	if _value < 0.0 then _value = 0.0; end;
	if _value > 1.0 then _value = 1.0; end;
	local done = (_w * _value);
	_panel:drawRectBorder(_x, _y, _w, _h, em_progressBar.border.a, em_progressBar.border.r, em_progressBar.border.g, em_progressBar.border.b);
	_panel:drawRect(_x, _y, _w, _h, em_progressBar.bg.a, em_progressBar.bg.r, em_progressBar.bg.g, em_progressBar.bg.b);
	_panel:drawRect(_x, _y, done, _h, em_progressBar.bar.a, em_progressBar.bar.r - _value, em_progressBar.bar.g + _value, em_progressBar.bar.b);
end

